public class Pica {

	public int siers;
	public int peperoni;
	public int ananass;
	public int olivas;
	public int senes;
	
	public Pica(int siersD, int peperoniD, int ananassD, int olivasD, int senesD) {
		this.siers = siersD;
		this.peperoni = peperoniD;
		this.ananass = ananassD;
		this.olivas = olivasD;
		this.senes = senesD;
	}
	
	public int pievienotSieru() {
		
		return siers;
	}
	public int pievienotPeperoni(int peperoni1) {
		
		return peperoni;
	}
	public int pievienotAnanasu(int ananass1) {
		
		return ananass;
	}
	public int pievienotOlivas(int olivas1) {
		
		return olivas;
	}
	public int pievienotSenes(int senes1) {
		
		return senes;
	}
}
